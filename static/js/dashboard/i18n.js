export const i18n = TEXT;

export const t = (path, fallback = "") => {
  const parts = path.split(".");
  let value = TEXT[lang] || TEXT.es;
  for (const part of parts) {
    if (value && typeof value === "object" && part in value) {
      value = value[part];
    } else {
      return fallback;
    }
  }
  return value ?? fallback;
};
